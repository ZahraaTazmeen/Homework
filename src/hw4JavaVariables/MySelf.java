package hw4JavaVariables;
/*
 For String, int, char, and boolean's naming convention, it has to start with a lower case letter and following words will start with upper case letters.  
 The String feature is putting words in quotes after an equal sign.
 The int feature is putting numbers after an equal sign. 
 The char feature is putting single upper case letters in single quotes after an equal sign.
 The boolean feature is putting true or false after an equal sign. 
 */

public class MySelf {

	public String myCity; // variable declared
	public String myName = "Zahraa"; // variable initialized
	public int myAge = 15; // variable initialized
	public char mySex = 'F'; // variable initialized
	public boolean fullTimeStudent = true; // variable initialized

}